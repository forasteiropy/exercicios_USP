# Faça um programa em Python que receba quatro notas, 
# calcule e imprima a média aritmética. Observe o exemplo abaixo:
print("Para calcular sua média final de provas, faça o que se pede:")
nota1=input("Insira a nota da sua primeira prova:")
nota2=input("Insira a nota da sua segunda prova:")
nota3=input("Insira a nota da sua terceira prova:")
nota4=input("Insira a nota da sua quarta prova:")
temp1=float(nota1)
temp2=float(nota2)
temp3=float(nota3)
temp4=float(nota4)
print("Sua média é:",(temp1+temp2+temp3+temp4)/4)
